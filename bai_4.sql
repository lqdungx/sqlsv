FROM CHUYENBAY 
WHERE GIODI < '12:00'
GROUP BY GADI

--CAU 4
SELECT N.MANV, N.TEN
FROM NHANVIEN N
JOIN CHUNGNHAN C ON N.MANV = C.MANV
GROUP BY N.MANV, N.TEN
HAVING COUNT(DISTINCT C.MAMB) > 3

--TRUY VAN LONG
--CAU 1
SELECT N.MANV AS [MÃ NV], N.TEN AS [NV KHÔNG LÀ PHI CÔNG] 
FROM NHANVIEN N
LEFT JOIN CHUNGNHAN C ON N.MANV = C.MANV
WHERE C.MAMB IS NULL

--CAU 2
SELECT TOP 1 N.MANV, N.LUONG
FROM NHANVIEN N
LEFT JOIN CHUNGNHAN C ON N.MANV = C.MANV
WHERE C.MAMB IS NULL 
ORDER BY LUONG DESC
