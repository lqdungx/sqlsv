FROM NHANVIEN

--CAU 9
SELECT C.MANV, M.LOAI
FROM CHUNGNHAN C
JOIN MAYBAY M ON C.MAMB = M.MAMB
WHERE LOAI LIKE '%BOEING%'

--CAU 10
SELECT N.MANV, N.TEN
FROM NHANVIEN N
JOIN CHUNGNHAN C ON N.MANV = C.MANV
WHERE C.MAMB = 747

--CAU 11
SELECT M.MAMB AS [MÃ MÁY BAY MÀ NV HỌ "NGUYỄN" LÁI]
FROM MAYBAY M
JOIN CHUNGNHAN C ON M.MAMB = C.MAMB
JOIN NHANVIEN N ON C.MANV = N.MANV
WHERE N.TEN LIKE N'%NGUYỄN%'

--CAU 12
SELECT N.MANV AS [MÃ NV CÓ THỂ LÁI CẢ 2 LOẠI BOEING VÀ AIRBUS]
FROM NHANVIEN N
JOIN CHUNGNHAN C ON N.MANV = C.MANV
JOIN MAYBAY M ON C.MAMB = M.MAMB
WHERE M.LOAI LIKE '%BOEING%' OR M.LOAI LIKE '%AIRBUS%'
GROUP BY N.MANV
HAVING COUNT(DISTINCT CASE WHEN M.LOAI LIKE '%BOEING%' THEN 1 END) > 0
   AND COUNT(DISTINCT CASE WHEN M.LOAI LIKE '%AIRBUS%' THEN 1 END) > 0;

--CAU 13
SELECT M.LOAI AS [LOẠI MÁY BAY THỰC HIỆN CHUYẾN VN280]
FROM MAYBAY M
JOIN CHUYENBAY C ON M.MAMB = C.MAMB
WHERE C.MACB = 'VN280'

--CAU 14
SELECT N'THÔNG TIN CHUYẾN BAY THỰC HIỆN BỞI AIRBUS 320' AS [THÔNG TIN], C.MACB, C.GADI, C.GADEN, C.DODAI, C.GIODEN, C.CHIPHI, C.MAMB, M.LOAI
FROM CHUYENBAY C
JOIN MAYBAY M ON C.MAMB = M.MAMB
WHERE C.MAMB = 320

--CAU 15
SELECT DISTINCT N.TEN AS [TÊN NHÂN VIÊN LÁI MÁY BAY BOEING]
FROM NHANVIEN N
JOIN CHUNGNHAN C ON N.MANV = C.MANV
JOIN MAYBAY M ON C.MAMB = M.MAMB
WHERE M.LOAI LIKE '%BOEING%'

